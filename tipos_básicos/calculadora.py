
# Calculadora basica

n1: int = input("Ingresa primer numero: ")
n2: int = input("Ingresa segundo numero: ")

# Conversion
n1 = int(n1)
n2 = int(n2)

suma: int = n1 + n2
resta: int = n1 - n2
mult: int = n1 * n2
div: int = n1 / n2

print(f"SUMA: {suma},  RESTA: {
      resta}, MULTIPLICACION: {mult}, DIVISION: {div}")
